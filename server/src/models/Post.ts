import { model, Schema } from 'mongoose'

import { IPost } from '../interfaces';

const postSchema = new Schema({
    body: String,
    username: String,
    createdAt: String,
    comments: [
        {
            body: String,
            username: String,
            createAt: String
        },
    ],
    likes: [
        {
            username: String,
            createdAt: String
        }
    ],
    user: {
        type: Schema.Types.ObjectId,
        ref: 'users'
    }
})

export default model<IPost>('Post', postSchema)